                        例如：我要學會編寫_____程式語言！我要學會操作_____軟體！
                        方法：收集大量資料，找尋重複關鍵字，最後以收集的關鍵字進行搜尋，這是發散到慢慢聚焦的過程。
                        <br><br>
                        M：measurable（可測量的）
                        要如何評量我有沒有達成目標呢？怎樣才算任務達成呢？
                        例如：我可以用_____程式語言寫出小遊戲！我可以用_____軟體畫出一幅畫！
                        方法：以目標關鍵字收集實作相關案例與相關教程，可嘗試以不同語言搜尋，獲得更多教學資源。
                        <br><br>
                        A：achievable（可實現的）
                        一步一步慢慢來，循序漸進，享受學習的過程。
                        例如：初學者階段的我，目前先達成_____階段，就已經很棒了！
                        方法：收集相關領域人士經驗談，紀錄學習進步的過程，和朋友分享，獲得建議與回饋。
                        <br><br>
                        R：relevant（相關的）
                        聚焦再發散，觸類旁通，讓知識與想法在你的生活紮根，堅持下去。
                        例如：_____程式語言可以應用在甚麼情況呢？業界使用的是_____軟體嗎？
                        方法：瀏覽求職網站，查詢相關行情。了解時事，隨時保持一顆求知的心。
                        <br><br>
                        T：time-bound（有時間限制的）
                        適度的壓力，是我前進的動力！幫自己設定一個期限，讓自己更有效率的達到目標！
